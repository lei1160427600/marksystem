CREATE TABLE COMMON_CONFIG(
	ID BIGINT NOT NULL AUTO_INCREMENT COMMENT 'id',
	COMPONENT_NAME VARCHAR(30) NOT NULL COMMENT 'component name',
	CONFIG_KEY VARCHAR(60) NOT NULL COMMENT 'config key',
	CONFIG_VALUE VARCHAR(256) DEFAULT NULL COMMENT 'config value',
	ENVIRONMENT VARCHAR(10) DEFAULT NULL COMMENT 'environment',
	DESCRIPTION VARCHAR(100) NOT NULL COMMENT 'config description',
	LAST_UPDATE_BY VARCHAR(60) NOT NULL COMMENT 'last update by',
	CREATE_TIMESTAMP TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT 'create time',
	LAST_UPDATE_TIMESTAMP TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT 'last update time',
	PRIMARY KEY (ID)
)ENGINE=INNODB AUTO_INCREMENT=1000 DEFAULT CHARSET=utf8 COMMENT='config table';