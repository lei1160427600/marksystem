CREATE DATABASE my_database default character set utf8 collate utf8_general_ci;

USE my_database;
--用户表
--UPDATETIME TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP NULL COMMENT '更新时间',
CREATE TABLE T_SYS_USERINFO(
	USER_ID BIGINT NOT NULL AUTO_INCREMENT COMMENT '用户ID',
	USER_NAME VARCHAR(50) NOT NULL COMMENT '姓名',
	USER_CODE VARCHAR(60) NOT NULL COMMENT '账号',
	CLASS VARCHAR(30) DEFAULT NULL COMMENT '班级',
	PROFESSION VARCHAR(40) DEFAULT NULL COMMENT '专业',
	USER_PWD VARCHAR(60) NOT NULL COMMENT '用户密码',
	AUTHORITY INT NOT NULL COMMENT'权限',
	CREATE_TIME TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT'创建时间',
	PRIMARY KEY (USER_ID)
)ENGINE=INNODB AUTO_INCREMENT=1000 DEFAULT CHARSET=utf8 COMMENT='用户信息表';

